name="chetanya"

print(name[0:4]) #goes from 0 to 4-1 i.e 0 to 3

print(name[2:-1]) #same as name[2:-1+size of string] i.e name[2:7]

#print(name[0:10:n]) skip n-1 character
print(name[0:10:1]) #skip 0 character
print(name[0:10:2]) #skip 1 character

print(name[:4]) #replace the first empty numner with 0
print(name[1:]) #replace the second empty number with length-1